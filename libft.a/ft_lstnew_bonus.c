#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*d;

	d = (t_list *)malloc(sizeof(t_list));
	if (!d)
		return (0);
	d->content = content;
	d->next = NULL;
	return (d);
}
